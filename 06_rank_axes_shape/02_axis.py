# Axis
dd=[
    [1,2,3],
    [4,5,6],
    [7,8,9]
]
# axis 1
print('\naxis 1:')
print('dd[0]:', dd[0])
print('dd[1]:', dd[1])
print('dd[2]:', dd[2])

# first index of axis 2
print('\nFirst index of axis 2:')
print('dd[0][0]:', dd[0][0])
print('dd[1][0]:', dd[1][0])
print('dd[2][0]:', dd[2][0])

# second index of axis 2
print('\nSecond index of axis 2:')
print('dd[0][1]:', dd[0][1])
print('dd[1][1]:', dd[1][1])
print('dd[2][1]:', dd[2][1])

# Third index of axis 2
print('\nThird index of axis 2:')
print('dd[0][2]:', dd[0][2])
print('dd[1][2]:', dd[1][2])
print('dd[2][2]:', dd[2][2])